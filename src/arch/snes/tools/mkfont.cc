static void write_data(const std::string& filename, int planes)
{
    std::fstream fd(filename, std::ios::binary | std::ios::out);

    auto it = font.begin();
    while (it != font.end())
    {
        uint8_t cdata[8];
        for (int i = 0; i < 8; i++)
            cdata[i] = *it++;

        auto output_data = [&]()
        {
            for (int i = 0; i < 8; i++)
            {
                uint8_t r = cdata[i];
                fd.put((char)r);
                fd.put(0);
            }
            for (int j = 2; j < planes; j += 2)
            {
                for (int i = 0; i < 8; i++)
                {
                    fd.put(0);
                    fd.put(0);
                }
            }
        };

        auto output_blank = [&]()
        {
            for (int j = 0; j < planes; j += 2)
            {
                for (int i = 0; i < 8; i++)
                {
                    fd.put(0);
                    fd.put(0);
                }
            }
        };

        if (planes == 2)
        {
            output_data();
            output_blank();
        }
        else
        {
            output_blank();
            output_data();
        }
    }
}

int main(int argc, const char* argv[])
{
    write_data(argv[1], 4);
    write_data(argv[2], 2);
    return 0;
}